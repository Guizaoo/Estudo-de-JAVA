package aulas1;

public class IfElse {
    static void main(String[] args) {
       double media = 1;

        if (media >= 9) {
            System.out.println("Excelente");
        } else if (media >= 7) {
            System.out.println("Bom");
        } else {
            System.out.println("Precisa melhorar");
        }

    }
}
